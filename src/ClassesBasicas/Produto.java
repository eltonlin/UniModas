package ClassesBasicas;

/**
 *
 * @author elton
 */
public class Produto {
    private int codProduto;
    private String tipoProduto;
    private String corProduto;
    private String descricaoProduto;
    private String tamanhoProduto;
    private float valorUnitarioProduto;
    
    public Produto(){
        
    }

    /**
     * @return the codProduto
     */
    public int getCodProduto() {
        return codProduto;
    }

    /**
     * @param codProduto the codProduto to set
     */
    public void setCodProduto(int codProduto) {
        this.codProduto = codProduto;
    }

    /**
     * @return the tipoProduto
     */
    public String getTipoProduto() {
        return tipoProduto;
    }

    /**
     * @param tipoProduto the tipoProduto to set
     */
    public void setTipoProduto(String tipoProduto) {
        this.tipoProduto = tipoProduto;
    }

    /**
     * @return the corProduto
     */
    public String getCorProduto() {
        return corProduto;
    }

    /**
     * @param corProduto the corProduto to set
     */
    public void setCorProduto(String corProduto) {
        this.corProduto = corProduto;
    }

    /**
     * @return the descricaoProduto
     */
    public String getDescricaoProduto() {
        return descricaoProduto;
    }

    /**
     * @param descricaoProduto the descricaoProduto to set
     */
    public void setDescricaoProduto(String descricaoProduto) {
        this.descricaoProduto = descricaoProduto;
    }

    /**
     * @return the tamanhoProduto
     */
    public String getTamanhoProduto() {
        return tamanhoProduto;
    }

    /**
     * @param tamanhoProduto the tamanhoProduto to set
     */
    public void setTamanhoProduto(String tamanhoProduto) {
        this.tamanhoProduto = tamanhoProduto;
    }

    /**
     * @return the valorUnitarioProduto
     */
    public float getValorUnitarioProduto() {
        return valorUnitarioProduto;
    }

    /**
     * @param valorUnitarioProduto the valorUnitarioProduto to set
     */
    public void setValorUnitarioProduto(float valorUnitarioProduto) {
        this.valorUnitarioProduto = valorUnitarioProduto;
    }
    
    
    
}
