    private void jButton_cadastrar_produtoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_cadastrar_produtoActionPerformed
        // TODO add your handling code here:
        try {
            Fachada fachada = new Fachada();
            Vendedor v = new Vendedor();
            v.setCpfVendedor(jTextFieldCpfVendedor.getText());
            v.setNomeVendedor(jTextFieldNomeVendedor.getText());
            v.setDataNascVendedor(jTextFieldDataVendedor.getText());
            if(jTextFieldSalarioVendedor.getText() != null && jTextFieldSalarioVendedor.getText().length() > 0 ){
               v.setSalario(Float.parseFloat(jTextFieldSalarioVendedor.getText()));
            }
            fachada.cadastraVendedor(v);
            JOptionPane.showMessageDialog(null, "Cadastrado com Sucesso !");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        
    }//GEN-LAST:event_jButton_cadastrar_produtoActionPerformed

