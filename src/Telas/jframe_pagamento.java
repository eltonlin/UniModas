    private void listarTipoPagamento() {
        try {
            Fachada fachada = new Fachada();      
            arrayPagamento = fachada.listarTipoPagamento();
            DefaultComboBoxModel model = new DefaultComboBoxModel();
            for (TipoPagamento tipoPagamento : arrayPagamento) {
                model.addElement(tipoPagamento.getDescTipoPagamento());
            }
            jComboBoxFormPagamento.setModel(model);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }
    }
    
    private void cadastrarPagamento(){
        try {
            Fachada fachada = new Fachada();            
            if(jTextFieldValorPagamento.getText() != null && jTextFieldValorPagamento.getText().length() > 0){                
            p.setValorPagamento(Float.parseFloat(jTextFieldValorPagamento.getText()));
            }
            p.setDataPagamento(jTextFieldDataPagamento.getText());
            fachada.cadastraPagamento(p);
            aceito = true;
            JOptionPane.showMessageDialog(null, "Cadastrado com Sucesso!");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }
    
