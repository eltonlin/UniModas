    public float calculaTotal(){
        float total = 0;
        if(listaItens != null){
           for (ListaItens l : listaItens) {
            total += l.getPreco();            
        }
           return total;
        }
        else{
            JOptionPane.showMessageDialog(null, "Lista Vazia");
            return total;
        }
          
    }
    
    
